<?php

namespace App\Entity;

use App\Constants;
use Doctrine\Common\Collections\ArrayCollection;
use Doctrine\Common\Collections\Collection;
use Doctrine\ORM\Mapping as ORM;

/**
 * @ORM\Entity(repositoryClass="App\Repository\TeamRepository")
 */
class Team
{
    /**
     * @ORM\Id()
     * @ORM\GeneratedValue()
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="string", length=255)
     */
    private $name;

    /**
     * @ORM\OneToMany(targetEntity="App\Entity\Project", mappedBy="team")
     */
    private $projects;

    /**
     * @ORM\OneToMany(targetEntity="App\Entity\Role", mappedBy="team")
     */
    private $roles;

    public function __construct()
    {
        $this->projects = new ArrayCollection();
        $this->roles = new ArrayCollection();
    }

    public function getId()
    {
        return $this->id;
    }

    public function getName(): ?string
    {
        return $this->name;
    }

    public function setName(string $name): self
    {
        $this->name = $name;

        return $this;
    }

    /**
     * @return Collection|Project[]
     */
    public function getProjects(): Collection
    {
        return $this->projects;
    }

    public function addProject(Project $project): self
    {
        if (!$this->projects->contains($project)) {
            $this->projects[] = $project;
            $project->setTeam($this);
        }

        return $this;
    }

    public function removeProject(Project $project): self
    {
        if ($this->projects->contains($project)) {
            $this->projects->removeElement($project);
            // set the owning side to null (unless already changed)
            if ($project->getTeam() === $this) {
                $project->setTeam(null);
            }
        }

        return $this;
    }

    /**
     * @return Collection|Role[]
     */
    public function getRoles(): Collection
    {
        return $this->roles;
    }

    public function addRole(Role $role): self
    {
        if (!$this->roles->contains($role)) {
            $this->roles[] = $role;
            $role->setTeam($this);
        }

        return $this;
    }

    public function removeRole(Role $role): self
    {
        if ($this->roles->contains($role)) {
            $this->roles->removeElement($role);
            // set the owning side to null (unless already changed)
            if ($role->getTeam() === $this) {
                $role->setTeam(null);
            }
        }

        return $this;
    }

    public function __toString()
    {
        return $this->getName();
    }


    public function isMember($user) {
        foreach ($user->getUserRoles() as $role) {
            if($user->getId() == $role->getUser()->getId()) {
                return true;
            }
        }
        return false;
    }


    public function isLeader($user) {
        foreach ($this->getRoles() as $role) {
            if($user->getId() == $role->getUser()->getId() && $role->getType() == Constants::LEADER) {
                return true;
            }
        }
        return false;
    }

    public function isAdmin($user) {
        foreach ($this->getRoles() as $role) {
            if($user->getId() == $role->getUser()->getId() && ($role->getType() == Constants::ADMIN || $role->getType() == Constants::LEADER)) {
                return true;
            }
        }
        return false;
    }

    public function isOnlyAdmin($user) {
        foreach ($this->getRoles() as $role) {
            if($user->getId() == $role->getUser()->getId() && $role->getType() == Constants::ADMIN) {
                return true;
            }
        }
        return false;
    }

}
